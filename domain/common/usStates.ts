/**
 * Get state label from abbreviation.
 * @param abbreviation - Two-letter state code
 * @returns Full state name or abbreviation if not found
 */
export function getStateLabel(abbreviation: string): string {
  const state = US_STATES.find(s => s.value === abbreviation);
  return state?.label ?? abbreviation;
}
